package org.nypl.simplified.books.reader;

/**
 * The type of font selected for the reader.
 */

public enum ReaderFontSelection
{
  /**
   * Use a sans-serif font.
   */

  READER_FONT_SANS_SERIF,

  /**
   * Use the OpenDyslexic3 font.
   */

  READER_FONT_OPEN_DYSLEXIC,

  /**
   * Use a serif font.
   */

  READER_FONT_SERIF
}
